// First numbers

// reads a number n, and prints all numbers between 0 and n.
// input: natural number n.
// output: Print in order all natural numbers between 0 and n.

#include <iostream>
using namespace std;

int main()
{
    int n;
    cin >> n;

    // print all the numbers
    for (int i = 0; i <= n; i++) cout << i << endl;
}